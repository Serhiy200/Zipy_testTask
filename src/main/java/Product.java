public class Product {
    //Variables for set with products
    public String name;
    public String price;
    public String discount;
    public String startedPrice;
    public String deliver;

    //give value for variables
    public Product(String name, String price, String discount, String startedPrice, String deliver) {
        this.name = name;
        this.price = price;
        this.discount = discount;
        this.startedPrice = startedPrice;
        this.deliver = deliver;
    }
}
